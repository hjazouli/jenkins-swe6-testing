import re
import sys
import os
from datetime import datetime

# --- CONFIGURATION ---
TARGET_DIRS = ["src/app", "src/bsw", "src/main.c"]
REPORT_OUTPUT = "build/quality-report.html"

# --- RULES DEFINITION (MISRA-C:2012 & Architecture) ---
RULES = {
    "MISRA_15_1": {"id": "Rule 15.1", "desc": "Goto shall not be used", "pattern": r"\bgoto\b", "sev": "CRITICAL"},
    "MISRA_21_3": {"id": "Rule 21.3", "desc": "Dynamic memory banned (malloc/free)", "pattern": r"\b(malloc|free|calloc|realloc)\s*\(", "sev": "CRITICAL"},
    "MISRA_20_1": {"id": "Rule 20.1", "desc": "Include directives should only be preceded by preprocessor directives or comments", "pattern": r"^#include.*", "sev": "WARNING"},
    "ARCH_LAYER": {"id": "ARCH_01", "desc": "Layer Violation: ASW must not include BSW private headers", "pattern": r'#include\s*".*bsw_private.*"', "sev": "CRITICAL"},
    "CODING_01": {"id": "STYLE_01", "desc": "Magic numbers detected (use defined constants)", "pattern": r"=\s*\d{2,};", "sev": "ADVISORY"}
}

