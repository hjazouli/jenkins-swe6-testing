def analyze():
    violations = []
    stats = {"total": 0, "critical": 0, "files": 0}
    
    for target in TARGET_DIRS:
        files_to_check = []
        if os.path.isfile(target): files_to_check.append(target)
        else:
            for root, _, files in os.walk(target):
                for f in files:
                    if f.endswith(('.c', '.h')): files_to_check.append(os.path.join(root, f))
        
        for filepath in files_to_check:
            stats["files"] += 1
            with open(filepath, 'r') as f:
                for ln, content in enumerate(f.readlines(), 1):
                    # Clean line for analysis
                    clean_content = content.split("//")[0].split("/*")[0].strip()
                    if not clean_content: continue
                    
                    for key, rule in RULES.items():
                        if re.search(rule["pattern"], clean_content):
                            v = {
                                "id": rule["id"],
                                "desc": rule["desc"],
                                "sev": rule["sev"],
                                "file": filepath,
                                "line": ln,
                                "snippet": clean_content[:50] + "..." if len(clean_content) > 50 else clean_content
                            }
                            violations.append(v)
                            stats["total"] += 1
                            if v["sev"] == "CRITICAL": stats["critical"] += 1

    generate_html_report(violations, stats)
    
    print(f"✅ Analysis Complete. Findings: {stats['total']} ({stats['critical']} Critical)")
    print(f"📊 Dashboard generated: {REPORT_OUTPUT}")
    
    if stats['critical'] > 0:
        sys.exit(1)
    sys.exit(0)

if __name__ == "__main__":
    analyze()
